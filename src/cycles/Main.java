package cycles;

public class Main {


    public static void main(String[] args) {
        //for
        for(int i = 0; i < 10; i++){
            System.out.println("Цикл работает " + (i + 1) + " раз.");
        }
        //while
        int i = 0;
        while (i < 10){
            System.out.println("Цикл работает " + (i + 1) + " раз.");
            i++;
        }

    }
}
